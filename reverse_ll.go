func reverse(llist *SinglyLinkedListNode) *SinglyLinkedListNode {
	// Write your code here
	var prev *SinglyLinkedListNode = nil
	curr := llist
	for curr != nil {
		nextUp := curr.next
		curr.next = prev
		prev = curr
		curr = nextUp
	}
	return prev
}

